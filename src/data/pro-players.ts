export function getPlayerBySlug(slug: string): ProPlayer | undefined {
  return proPlayers.find(p => p.slug === slug);
}

// 获取所有选手 slugs
export function getAllPlayerSlugs(): string[] {
  return proPlayers.map(p => p.slug);
}

